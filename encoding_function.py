def split_string(s):
    # List comprehension to split the string into chunks of 2 characters
    return [s[i:i+2] for i in range(0, len(s), 2)]


def encode_number(number):
    str_converted = str(number)
    if len(str_converted) % 2 != 0:
        str_converted = "0%s" % str_converted
    items = split_string(str_converted)
    encoded_str = ""
    for item in items:
        encoded_str = "%s%s" % (encoded_str, encoding_dict[item])
    return encoded_str
